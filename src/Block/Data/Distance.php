<?php

namespace Nirbose\PhpMcServ\Block\Data;

trait Distance
{
    protected int $distance = 1;

    /**
     * @return int
     */
    public function getDistance(): int
    {
        return $this->distance;
    }

    /**
     * @param int $distance
     */
    public function setDistance(int $distance): void
    {
        $this->distance = $distance;
    }
}